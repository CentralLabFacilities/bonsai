package de.unibi.citec.clf.bonsai.gui.grapheditor.api

/**
 * A set of styles that can be applied to a particular connector.
 */
enum class GConnectorStyle {

    /**
     * Default style
     */
    DEFAULT,

    /**
     * Drag over allowed
     */
    DRAG_OVER_ALLOWED,

    /**
     * Drag over forbidden
     */
    DRAG_OVER_FORBIDDEN
}