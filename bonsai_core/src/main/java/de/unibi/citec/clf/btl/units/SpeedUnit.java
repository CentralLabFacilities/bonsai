package de.unibi.citec.clf.btl.units;



/**
 * This {@link Enum} is supposed to keep track of the meaning of speed values.
 * Any methods using speed values should let the user specify if the given or
 * requested value is a m/s value or a km/h value.
 * 
 * @author lziegler
 */
public enum SpeedUnit {
	METER_PER_SEC, KILOMETER_PER_HOUR
}
